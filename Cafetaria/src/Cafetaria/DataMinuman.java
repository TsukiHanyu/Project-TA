    private void autonumber(){
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM minuman ORDER BY ID_Minuman DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoStruck = r.getString("ID_Minuman").substring(2);
                String MN = "" +(Integer.parseInt(NoStruck)+1);
                String Nol = "";
                
                if (MN.length()==1) 
                    {Nol = "00";}
                else if(MN.length()==2)
                    {Nol = "0";}
                else if(MN.length()==3)
                    {Nol = "";}
                
                txIDMinuman.setText("MN" + Nol + MN);  
            }else{
                txIDMinuman.setText("MN001");
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println("autonumber error");
        }
    }
    
